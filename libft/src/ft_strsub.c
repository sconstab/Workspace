#include "../includes/libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char *ns;

	if (!s)
		return (NULL);
	if (!(ns = malloc(len + 1 * sizeof(char))))
		return (NULL);
	ft_strncpy(ns, s + start, len);
	ns[len] = '\0';
	return (ns);
}
